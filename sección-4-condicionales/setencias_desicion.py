# conditional statements
value = True
if value:
    print('Value is True')
else:
    print('Value is False')
# conditional statements with multiple conditions
value1 = True
value2 = False
if value1 and value2:
    print('Both values are True')
elif value1 or value2:
    print('At least one value is True')
else:
    print('Both values are False')
# conditional statements with nested conditions
value1 = True
value2 = False
if value1:
    if value2:
        print('Both values are True')
    else:
        print('Value1 is True and Value2 is False')
else:
    print('Value1 is False')
